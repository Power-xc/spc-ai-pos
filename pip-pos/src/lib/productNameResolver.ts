/**
 * 상품명만 정제합니다.
 * - ? (mojibake) 제거
 * - [JBOD] 접두사 제거
 * - 공백 정리
 */
function cleanProductName(name: string): string {
  return String(name)
    .replace(/\?+/g, "")
    .replace(/^\[JBOD\]\s*/i, "")
    .replace(/\s+/g, " ")
    .trim();
}

/**
 * 단일 문자열 입력을 상품명으로 resolve합니다.
 * 숫자만으로 구성된 경우 PRODUCT_CODE_NAME_MAP에서 조회하고,
 * 아니라도 fallback으로 조회한 후 실패 시 원문을 반환합니다.
 */
export function resolveProductDisplayName(value: string | null | undefined): string {
  const normalized = String(value ?? "").trim();
  const resolved = PRODUCT_CODE_NAME_MAP[normalized] ?? normalized;
  return cleanProductName(resolved);
}

/**
 * 여러 필드가 있는 객체에서 상품명과 코드를 추출해 표시용 이름을 반환합니다.
 *
 * 우선순위:
 * 1. name 필드 그룹(productName, product_name, name) 중 첫 번째 유효값
 * 2. id 필드 그룹(productId, product_id, code) 중 첫 번째 유효값
 * 3. name이 숫자 코드인 경우 코드 조회 → id 조회 → "상품명 확인 필요"
 * 4. name이 비어있고 id가 있는 경우 id로 조회
 * 5. name이 유효한 경우 정제 후 반환
 */
export function resolveProductDisplay(input: {
  productName?: string | null;
  product_name?: string | null;
  name?: string | null;
  productId?: string | null;
  product_id?: string | null;
  code?: string | null;
}): string {
  // 이름 추출 (우선순위: productName > product_name > name)
  const rawName = input.productName ?? input.product_name ?? input.name;
  const name = rawName ? String(rawName).trim() : "";

  // ID 추출 (우선순위: productId > product_id > code)
  const rawId = input.productId ?? input.product_id ?? input.code;
  const id = rawId ? String(rawId).trim() : "";

  // 이름이 있는 경우
  if (name) {
    // 이름이 숫자 코드인 경우
    if (/^\d+$/.test(name)) {
      const byName = PRODUCT_CODE_NAME_MAP[name];
      if (byName) return cleanProductName(byName);
      // 코드 조회 실패 시 id로도 시도
      if (id) {
        const byId = PRODUCT_CODE_NAME_MAP[id];
        if (byId) return cleanProductName(byId);
      }
      return "상품명 확인 필요";
    }

    return cleanProductName(name);
  }

  // 이름이 없고 id만 있는 경우
  if (id) {
    return cleanProductName(PRODUCT_CODE_NAME_MAP[id] ?? id);
  }

  return "상품명 확인 필요";
}

/**
 * 설명 문장에 포함된 6자리 숫자 상품 코드를 상품명으로 치환합니다.
 * 교체된 상품명은 정제하되, 문장 자체의 물음표는 유지합니다.
 *
 * 치환 규칙:
 * - \b로 시작하는 연속 6자리 숫자문자만 대상
 * - PRODUCT_CODE_NAME_MAP에 등록된 코드만 치환
 * - 없는 코드는 원문 유지
 * - 치환된 상품명만 ? 제거 정제 적용
 * - 날짜(2026), 시간(13:00), 퍼센트(+12.5%), 금액(1,489,959)은 6자리가 아니므로 안전
 */
export function resolveProductNamesInText(text: string | null | undefined): string {
  if (!text) return "";
  return String(text).replace(/\b(\d{6})\b/g, (code) => {
    const resolved = PRODUCT_CODE_NAME_MAP[code] ?? code;
    return cleanProductName(resolved);
  });
}
